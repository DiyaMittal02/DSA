import java.util.Scanner;
public class switchn{
    public static void main(String[] args){
        int num = 2;
        switch (num){
            case 1: 
            System.out.println("First");
            break;
            case 2: 
            System.out.println("Second");
            break;
            default:
                System.out.println("This is a default case");
        }
    }
}